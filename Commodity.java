package commodity;

public class Commodity {
	int ProductID;
	String ProductName;
	String InnerCName;
	float Price;
	String UnitPrice;
	String Specification;
	float Weight;
	String DetailedInfo;
	String UpshelfDate;
	int StockLevel;
	float PricePromotion;

	public int getProductID() {
		return ProductID;
	}

	public void setProductID(int ProductID) {
		this.ProductID = ProductID;
	}

	public String getProductName() {
		return ProductName;
	}

	public void setProductName(String ProductName) {
		this.ProductName = ProductName;
	}

	public String getInnerCName() {
		return InnerCName;
	}

	public void setInnerCName(String InnerCName) {
		this.InnerCName = InnerCName;
	}

	public float getPrice() {
		return Price;
	}

	public void setPrice(float Price) {
		this.Price = Price;
	}

	public String getUnitPrice() {
		return UnitPrice;
	}

	public void setUnitPrice(String UnitPrice) {
		this.UnitPrice = UnitPrice;
	}

	public String getSpecification() {
		return Specification;
	}

	public void setSpecification(String Specification) {
		this.Specification = Specification;
	}

	public float getWeight() {
		return Weight;
	}

	public void setWeight(float Weight) {
		this.Weight = Weight;
	}

	public String getDetailedInfo() {
		return DetailedInfo;
	}

	public void setDetailedInfo(String DetailedInfo) {
		this.DetailedInfo = DetailedInfo;
	}

	public String getUpshelfDate() {
		return UpshelfDate;
	}

	public void setUpshelfDate(String UpshelfDate) {
		this.UpshelfDate = UpshelfDate;
	}

	public int getStockLevel() {
		return StockLevel;
	}

	public void setStockLevel(int StockLevel) {
		this.StockLevel = StockLevel;
	}

	public float getPricePromotion() {
		return PricePromotion;
	}

	public void setPricePromotion(float PricePromotion) {
		this.PricePromotion = PricePromotion;
	}

	

}
